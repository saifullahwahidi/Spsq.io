// FNG 



const FNGElement = document.getElementById('BTC__FNG'); 

fetch('https://api.alternative.me/fng/')
  .then(response => response.json())
  .then(data => {
    const fngValue = data.data[0].value;
    FNGElement.innerHTML = `${fngValue}`; // update the value of the FNG element

    if (fngValue >= 10 && fngValue < 15) {
      FNGElement.style.color = '#ff0e00';
    } else if (fngValue >= 15 && fngValue < 30) {
      FNGElement.style.color = '#ff1600';
    } else if (fngValue >= 30 && fngValue < 40) {
      FNGElement.style.color = 'red';
    } else if (fngValue >= 40 && fngValue < 65) {
      FNGElement.style.color = '#59cd60';
    } else if (fngValue >= 65 && fngValue < 70) {
      FNGElement.style.color = 'red';
    } else if (fngValue >= 70 && fngValue < 80) {
      FNGElement.style.color = 'green';
    } else if (fngValue >= 80 && fngValue < 90) {
      FNGElement.style.color = '#1fbc00';
    } else if (fngValue >= 90 && fngValue <= 100) {
      FNGElement.style.color = '#2fff06';
    }
  })
  .catch(error => console.error(error));























